function main(){
    var sections = document.getElementsByTagName("section");
    var wHeight = Math.max(window.innerHeight,document.body.clientHeight || document.documentElement.clientHeight);
    for(var i=0;i<sections.length;i++){
        sections[i].style.height = wHeight+"px";
    }
    var j = 0;
    domScroll(function(event){
        if(event.delta>0){
            j--;
            if(j==-1){
                j = sections.length-1;
            }
            runScrollTo(wHeight*j);
        }else if(event.delta<0){
            j++;
            if(j==sections.length){
                j=0;
            }
            runScrollTo(wHeight*j);
        }
    });
    var test = document.getElementById("test");
    var animate = new DSAnimate(test,[
        {
            attribute : "width",
            to : 600
        }
    ],500);
    animate.do();
}

